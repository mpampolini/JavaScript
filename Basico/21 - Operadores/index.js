/*
 * Operadores de comparação
 * > maior que
 * < menor que
 * >= maior que ou igual a
 * <= menor que ou igual a
 * === igualdade estrita (valor e tipo) - Recomendado
 * !== deferente estrito  (valor e tipo) - Recomendado
 * == igualdade (compara apenas VALOR) ********* Não recomendado
 * != deferente  (compara apenas VALOR) ********* Não recomendado
 */

const num1 = 10;
const num2 = "10";
const comp = num1 !== num2;
